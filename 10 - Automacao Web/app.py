import webbrowser
#Abrir uma aba no navegador existente
webbrowser.open("https://www.google.com.br/")

#Abrir uma nova aba em um navegador existente ou criar outro
webbrowser.open_new_tab("https://www.google.com.br/")

#Criar e abrir uma nova janela do zero
webbrowser.open_new("https://www.google.com.br/")